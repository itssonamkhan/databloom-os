export const excelToolkitCategories = [
  "Keyboard Shortcuts",
  "Excel Tricks",
  "Formula Alternatives",
  "Productivity Tips",
  "Analyst Secrets",
] as const;

export const excelToolkitDifficulties = [
  "Beginner",
  "Intermediate",
  "Advanced",
] as const;

export type ExcelToolkitCategory = (typeof excelToolkitCategories)[number];
export type ExcelToolkitDifficulty = (typeof excelToolkitDifficulties)[number];

type ExcelToolkitBase = {
  id: string;
  title: string;
  category: ExcelToolkitCategory;
  difficulty: ExcelToolkitDifficulty;
};

export type ExcelShortcutGroup =
  | "Navigation"
  | "Selection"
  | "Editing"
  | "Formatting"
  | "Formulas"
  | "Tables and filters"
  | "Workbook management";

export const excelShortcutGroups: ExcelShortcutGroup[] = [
  "Navigation",
  "Selection",
  "Editing",
  "Formatting",
  "Formulas",
  "Tables and filters",
  "Workbook management",
];

export type ExcelShortcut = ExcelToolkitBase & {
  kind: "shortcut";
  group: ExcelShortcutGroup;
  windows: string;
  mac: string;
  explanation: string;
  example?: string;
};

export type ExcelTrick = ExcelToolkitBase & {
  kind: "trick";
  whatItDoes: string;
  whenToUse: string;
  steps: string[];
  example: string;
  commonMistake: string;
};

export type FormulaAlternative = ExcelToolkitBase & {
  kind: "comparison";
  traditionalMethod: string;
  modernAlternative: string;
  traditionalFormula: string;
  alternativeFormula: string;
  useTraditionalWhen: string;
  useAlternativeWhen: string;
  compatibility: string;
  performance: string;
  commonMistake: string;
};

export type ProductivityTip = ExcelToolkitBase & {
  kind: "productivity";
  summary: string;
  actions: string[];
  example: string;
};

export type AnalystSecret = ExcelToolkitBase & {
  kind: "analyst-secret";
  practice: string;
  whyItMatters: string;
  action: string;
};

export type ExcelToolkitItem =
  | ExcelShortcut
