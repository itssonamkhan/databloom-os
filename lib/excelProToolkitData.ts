export function getExcelToolkitSearchValues(item: ExcelToolkitItem) {
  const common = [item.title, item.category, item.difficulty];
  switch (item.kind) {
    case "shortcut":
      return [
        ...common,
        item.group,
        item.windows,
        item.mac,
        item.explanation,
        item.example ?? "",
      ];
    case "trick":
      return [
        ...common,
        item.whatItDoes,
        item.whenToUse,
        item.example,
        item.commonMistake,
        ...item.steps,
      ];
    case "comparison":
      return [
        ...common,
        item.traditionalMethod,
        item.modernAlternative,
        item.traditionalFormula,
        item.alternativeFormula,
        item.useTraditionalWhen,
        item.useAlternativeWhen,
        item.compatibility,
        item.performance,
        item.commonMistake,
      ];
    case "productivity":
      return [...common, item.summary, item.example, ...item.actions];
    case "analyst-secret":
      return [
        ...common,
        item.practice,
        item.whyItMatters,
        item.action,
      ];
  }
}
